#   13. Create a set and perform union, intersection, and difference. 

a = {1, 2, 3, 4, 5}
b = {4, 5, 6, 7, 8}

# Union
union = a | b
print("Union:", union)

# Intersection
intersection = a & b
print("Intersection:", intersection)

# Difference
difference = a - b
print("Difference(a - b):", difference)

# Difference
difference = b - a
print("Difference(b - a):", difference)
